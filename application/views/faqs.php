                        Q: HOW SUCCESSFUL & SAFE IS IMPLANT TREATMENT?
                     </div>
                     <div class="accordion-content">
                        <p>
                        Modern dental implant treatment is highly successful, as a general guideline, the five-year success rate of dental implant is over 95% with the lower jaw (mandible) and about 90% with upper jaw (maxilla). The success rates may slowly decrease, as the implant stays longer in function. In difficult cases, the success rates are expected to be lower. Despite the overall high success rates with dental implant treatment, there is still the possibility of failure, which can be contributed by various factors and conditions, such as poor healing power, poor bone quality, smoking and diabetes, and most important of all -poor oral hygiene. Dental Implants are perfectly safe. With proper maintenance they normally last for up to fifteen years while some companies claim lifelong warranty. Regular dental checkups and diligently maintaining one’s oral hygiene is of utmost importance in preventing failure.
                        </p>
