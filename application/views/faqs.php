                        Q: What is periodontal disease?
                     </div>
                     <div class="accordion-content">
                        <p>Periodontal disease is inflammation and infection of the gums and supporting bone structure, which if left untreated, can cause permanent jaw bone destruction and possible tooth mobility with eventual loss. Untreated periodontal disease has been linked to increased risk for systemic conditions such as heart disease, stroke, low birth weight babies and pre-term delivery, respiratory disease, and prostate cancer. An advanced stage of periodontal disease exhibits swollen gums pulling away from your bone and teeth. Other signs of periodontal disease include:</p>
                        <div class="eye-care-about-content ppl-0">   
                            <ul class="list">
                                <li><i class="bx bx-check"></i> Bad breath</li>
                                <li><i class="bx bx-check"></i> Red or swollen gums</li>
                                <li><i class="bx bx-check"></i> Loose teeth</li>
                                <li><i class="bx bx-check"></i> Sensitive teeth</li>
                                <li><i class="bx bx-check"></i> Pus coming from around the teeth</li>
                                <li><i class="bx bx-check"></i> Pain on chewing</li>
                                <li><i class="bx bx-check"></i> Tender gums</li>
                                <li><i class="bx bx-check"></i> Bleeding gums</li>                                               
                            </ul>                    
                        </div>
                        <p>
                        Treatment of early periodontal disease can be performed by regular cleaning whereas advanced stages may require surgery. Periodontal disease can be prevented and treated successfully by visiting your dentist every six months and following recommended care plans.
                        </p>
