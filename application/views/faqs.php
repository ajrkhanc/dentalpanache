                        Q: WHO CAN HAVE DENTAL IMPLANTS & ARE THEY PAINFUL?
                     </div>
                     <div class="accordion-content">
                        <p>
                        Every procedure in dentistry is performed after thorough numbing of the adjoining areas. The discomfort felt during dental implant placement is no more than any surgical extraction. Some amount of pain and swelling is expected after the procedure for 24-48 hours which is managed by appropriate medication. A temporary crown will be provided to take care of aesthetic appearance. Dental implants can be safely placed in almost every case after thorough investigation and treatment planning. With recent advances, even patients who have low sinus cavities or a reduced level of bone density are now able to have dental implants. We provide sinus lifts and bone reconstruction to patients who have these issues to facilitate placement of dental implants in the best possible position, avoiding nerve canals and sinus cavities.
                        </p>
