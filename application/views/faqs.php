                        Q: Why is flossing important?
                     </div>
                     <div class="accordion-content">
                        <p>You should floss to reduce the number of bacteria feeding on the food particles retained on your teeth. It is extremely important to floss at least once a day to clean the areas between the gums and teeth where the toothbrush cannot reach. If you do not floss, you allow plaque to accumulate on your teeth which eventually hardens into tartar. Plaque can be removed by brushing but only your dentist can remove tartar.</p>
                     </div>
