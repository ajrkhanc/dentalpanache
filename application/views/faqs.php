                        <i class='bx bx-plus'></i>
                        Q: What can I do about sensitive teeth?
                     </div>
                     <div class="accordion-content">
                        <p>Anti-sensitivity toothpaste, which contains strontium chloride or potassium nitrate are very effective in treating sensitive teeth. They are usually recommended for a few weeks/months followed by regular toothpaste as continuous use makes it ineffective. Highly acidic foods such as oranges, grapefruits and lemons, as well as tea and soda can increase tooth sensitivity, and work against any sensitivity toothpaste. If you do not get relief by brushing gently and using a desensitizing toothpaste, see your dentist. There are special compounds that can be applied in office to the exposed roots of your tooth to reduce the sensitivity. This procedure has to be repeated as and when the symptoms appear.</p>
                     </div>
