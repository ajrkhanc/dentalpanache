                        Q: What should I do about bleeding gums?
                     </div>
                     <div class="accordion-content">
                        <p>Inadequate oral hygiene, stress, pregnancy, familial predisposition, chronic smoking, orthodontic treatment are some of the common reasons for bleeding gums. Usually, gums that bleed are a symptom of the onset of periodontal disease or gingivitis. But often, people stop brushing frequently and effectively because it may be painful or it may cause the gums to bleed again. Instead, when gums are inflamed, brushing often and effectively is imperative. More importantly, you should see your dentist to have a periodontal screening and recording performed in order to determine the level of disease present and the best treatment course to pursue.</p>
