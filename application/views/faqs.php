                        Q: AT WHAT AGE SHOULD I START TAKING MY CHILD TO THE DENTIST?
                     </div>
                     <div class="accordion-content">
                        <p>
                        It is usually advisable to bring a child to the dentist within six months of eruption of the first tooth. Parents should make an earnest attempt to expose the child to the dental office at an early age so that he/she will learn that visiting the dentist is a regular part of health care. Do not instill your own fear or anxiety into your child’s mind by being nervous. When you talk to your child about going to the dentist, explain what will happen without adding things like “it won’t hurt” or “don’t be scared.”. Make the first visit a positive one – this helps build a relationship on trust and respect and can prove invaluable if your child needs to be treated later for any dental problem. Be sure to get an early start on regular dental care at home. Start cleaning your child’s mouth with a soft damp cloth before teeth come in and continue with a soft toothbrush once he or she has a first tooth. Limit sugary treats and focus on healthy food choices from the very beginning.
                        </p>
