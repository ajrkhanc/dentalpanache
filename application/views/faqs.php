                        Q: How long does the effect of anaesthesia last?
                     </div>
                     <div class="accordion-content">
                        <p>
                        When anesthesia has been used, your lips, teeth, and tongue may be numb for several hours after the appointment. Avoid any chewing until the numbness has completely worn off. It is easy to bite or burn your tongue or lip while numb. It is recommended that you take some pain killer (1-2 tablets every 4-6 hours as needed) before the anesthetic effect completely wears off. This will help to relieve any swelling or pain at the injection sites where the anesthetic was administered.
                        </p>
