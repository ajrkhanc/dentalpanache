                        Q: Why should I visit the dentist regularly?
                     </div>
                     <div class="accordion-content">
                        <p>
                        Many people do not see a dentist on a regular basis. They go only when they have a problem. We call this “crisis treatment” as opposed to “preventive treatment.” While these patients may feel they are saving money, it usually ends up costing much more time and agony. The reason for this is that most dental problems do not have any symptoms until they reach the advanced stages of the disease process. A simple example is tooth decay. A tooth decay may not hurt until it approaches the nerve of the tooth. By that time, root canal treatment followed by a post, buildup, and crown are often necessary, instead of the filling which could have been placed several years earlier when the cavity was just beginning to form. During the check-up, your dental professional will check your overall oral health for any trouble areas. During the cleaning, there is removal of any plaque and tartar buildup and polishing of your teeth. You should have a regular dental visit at least twice a year.
                        </p>
