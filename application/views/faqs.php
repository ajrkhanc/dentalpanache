                        Q: WHY SHOULD I REPLACE A MISSING TOOTH?
                     </div>
                     <div class="accordion-content">
                        <p>
                        Missing a single tooth can, over time, allow the misalignment of your remaining teeth and thus erode your current smile. A single tooth replaced with a dental implant and crown is the most predictable, conservative and long-lasting method of tooth replacement. Without a dental implant, the two adjacent teeth to a missing tooth must be reduced to anchor a bridge. Dental implants eliminate the need to modify natural teeth.
                        </p>
