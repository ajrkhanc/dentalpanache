                        Q: Do over the counter whitening products work?
                     </div>
                     <div class="accordion-content">
                        <p>
                        Commercial whitening toothpastes, strips or gels vary greatly in their ability to whiten teeth. They work by removing surface stains from the teeth with the use of mild abrasives without altering the intrinsic color of the teeth. These whitening agents may use harsh abrasives which on frequent use can damage tooth enamel and subsequently lead to tooth sensitivity. It is important to understand that tooth colored fillings and crowns cannot be bleached. It is imperative to consult with your dentist about the right bleaching protocol for you.
                        </p>
