                        Q: How do I care for my crown/bridge?
                     </div>
                     <div class="accordion-content">
                        <p>
                        A crown is needed on a tooth when the tooth is weakened by either decay, fracture or a very large restoration. Root canal treatment is completed by the placement of a crown on the tooth. A crown covers and protects the tooth from breaking in the future. Multiple crowns joined together constitute a bridge. After cementation of crown/bridge, the gums may be sore for few days. Rinse three times a day with warm salt water to reduce pain and swelling. Use medication only as directed. It is recommended to start eating soft food with the new crown and then gradually use it like any other natural tooth once you get used to it. It’s important to continue to brush normally, but floss very carefully and pull the floss out from the side. If your bite feels uneven, if you have persistent swelling or pain, or you have any other questions or concerns, please visit us as soon as possible.
                        </p>
