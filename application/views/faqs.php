                        Q: What do I do after a root canal?
                     </div>
                     <div class="accordion-content">
                        <p>
                        Root canal therapy can be done in a single sitting but it often takes two or more appointments to complete. A temporary filling is placed to protect the tooth between appointments. After each appointment when anesthetic has been used, avoid chewing until the numbness has completely off to avoid hurting/burning your teeth, lips and tongue. Between appointments it is common (and not a problem) for a small portion of your temporary filling to wear away or break off. If the entire filling falls out, or if a temporary crown comes off, call us so that it can be replaced. It’s normal to experience some discomfort for several days after a root canal appointment, especially when chewing. To control discomfort, take pain medication as recommended. To further reduce pain and swelling, rinse three times a day with lukewarm saline water (a tsp. of salt in a cup of warm water, rinse-swish-spit). If antibiotics are prescribed, continue to take them as prescribed, even if all symptoms and signs of infection are gone. To protect the tooth and help keep your temporary in place, avoid eating sticky foods (especially gum), hard foods, and if possible, chew only on the opposite side of your mouth. It’s important to continue to brush and floss normally.
                        </p>
                     </div>
                  </div>

                  <div class="accordion-item">
                     <div class="accordion-title">
                        <i class='bx bx-plus'></i>
                        Q: What do I do after an extraction?
                     </div>
                     <div class="accordion-content">
                        <p>
                        After an extraction, it’s important for a blood clot to form to stop the bleeding and begin the healing process. That’s why we ask you to bite on a gauze pad for 30 to 45 minutes after the extraction. If bleeding or oozing continues after you remove the gauze pad, place another gauze pad and bite firmly for another thirty minutes. After that remove the gauze pad and eat ice cream. For the next 24 hours, eat soft, bland and cold food. Avoid anything hot, hard or spicy. Drink lots of fluids and avoid alcoholic or carbonated beverages. Don’t: smoke, suck through a straw, rinse your mouth vigorously, spit or clean the teeth next to the extraction site. These activities will dislodge the clot and slow down healing. After the tooth is extracted you may feel some pain and have some swelling. Ice packs are recommended for the first 24 hours intermittently. The swelling usually starts to go down after 48 hours. If antibiotics and pain killers are prescribed, continue to take them for the indicated length of time. After 24 hours, gently rinse your mouth with lukewarm salt water three times a day for 1-2 weeks to facilitate healing. Also, rinse gently after meals to keep food out of the extraction site. It is very important to resume your normal dental routine after 24 hours which include brushing your teeth and tongue and flossing at least once a day. This speeds healing and helps keep your breath and mouth fresh. Some oozing and discoloration of the saliva is normal.
                        </p>
